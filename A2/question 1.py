def sign(M,n,d):
    signature = (M**d)%n
    return signature

def verify(S,n,e,M):
    M_1 = (S**e)%n
    if M_1 == M:
        return True
    return False

def blindsign(x,n,M,e,d):
    M_b = (x**e)*M%n
    s_b = (M_b**d)%n
    s = (s_b/x)%n
    return s


